function add(a,b){
    return a+b;
}
function Substract(a,b){
    return a-b;
}
function Mul(a,b){
    return a*b;
}
function Divide(a,b){
    return a/b;
}
console.log("Addition",add(5,10));
console.log("Subtraction",Substract(10,5));
console.log("Multiply",Mul(5,10));
console.log("Divide",Divide(10,5));
